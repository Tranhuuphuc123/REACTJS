/*
 >>>>KHÁI NIỆM<<<<
 => Năm ra đời 2015, là phiên bản thứ 6 của JS ES6
 => ECMAScript 6 (ES6), cũng được gọi là ECMAScript 2015, là một phiên bản tiêu chuẩn mới nhất
  của ngôn ngữ lập trình JavaScript, được ra mắt vào năm 2015. 
 => ES6 đánh dấu một bước tiến quan trọng trong sự phát triển của JavaScript, bổ sung nhiều tính 
 năng mới và cải thiện ngôn ngữ để làm cho việc lập trình JavaScript trở nên hiệu quả hơn, đơn giản
  hơn và dễ đọc hơn.

 ======================================================================
  => đây là các tính năng nổi bật của của ECMASript6
  1/ Let. const: 
    --> thay thế khai báo var, let khai báo trong phạm vi khối mã

  2/ Template Literals:
    --> cho phép bạn tạo chuổi đa dòng và nhúng biến vào một chuỗi dễ dàng với cú pháp:
    `${}`

  3/ Multi-line String:
    --> khái niệm viết nhiều dòng -> nó kết hợp và bổ trợ cho chức năg số 2 Template literals

  4/ Arrow function:
    --> cú pháp ngắn gọn viết bằng dấu =>

  5/ Classes:
    --> ES6 đưa ra cú pháp mới định nghĩa lớp(class)-> hộ trợ tốt OOP

  6/ Default parameter values:
    --> Bây giờ có thể đặt giá trị mặc định cho các tham số của hàm, giúp viết 
    mã ít lỗi hơn

  7/ Destructuring:
    --> ES6 cho phép bạn trích xuất giá trị từ một đối tượng hoặc mẳng và gán 
    chúng vào các biến riêng lẻ một cách dễ dàng.

  8/ Rest parameters:
    --> Giúp bạn gom các phần tử riêng lẻ thành một mảng hoặc đối tượng

  9/ Spread:
    --> cho phép bạn mở rộng một mảng hoặc đối tượng thành các phần tử riêng lẻ

  10/ Enhanced object literals
    => Tính năng này giúp mình làm các việc sau:
    + Định nghĩa key-value cho Object
    + Định nghĩa method cho Object
    + Định nghĩa key cho Object dưới dạng biến
    
  11/ Tagged template literal
  12/ Modules:
    --> ES6 giới thiệu hệ thống module tiêu chuẩn để quản lý mã nguồn trong 
    các dự án lớn, giúp tách biệt và tái sử dụng mã dễ dàng hơn.
*/



